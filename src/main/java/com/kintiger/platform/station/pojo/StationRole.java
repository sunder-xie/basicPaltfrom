package com.kintiger.platform.station.pojo;

public class StationRole {

	private String stationRoleId;
	private String stationId;
	private String roleId;

	public String getStationRoleId() {
		return stationRoleId;
	}

	public void setStationRoleId(String stationRoleId) {
		this.stationRoleId = stationRoleId;
	}

	public String getStationId() {
		return stationId;
	}

	public void setStationId(String stationId) {
		this.stationId = stationId;
	}

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}

}

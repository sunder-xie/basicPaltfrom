package com.kintiger.platform.report.pojo;

public class ReportStructure {
	/**
	 * 主键
	 */
	private long STRUCTURE_ID;
	/**
	 * 报表编号
	 */
	private String REPORT_NO;
	/**
	 * 报表结构
	 */
	private String REPORT_HEADER;
	/**
	 * 数据来源
	 */
	private String REPORT_CONTENT_SOURCE;
	
	private String REPORT_HEADER_ITEM;
	
	private String REPORT_HEADER_CODE;
	
	private String REPORT_HEADER_NAME;
	
	public String getREPORT_NO() {
		return REPORT_NO;
	}
	public void setREPORT_NO(String rEPORTNO) {
		REPORT_NO = rEPORTNO;
	}
	public String getREPORT_HEADER() {
		return REPORT_HEADER;
	}
	public void setREPORT_HEADER(String rEPORTHEADER) {
		REPORT_HEADER = rEPORTHEADER;
	}
	public String getREPORT_CONTENT_SOURCE() {
		return REPORT_CONTENT_SOURCE;
	}
	public void setREPORT_CONTENT_SOURCE(String rEPORTCONTENTSOURCE) {
		REPORT_CONTENT_SOURCE = rEPORTCONTENTSOURCE;
	}
	public long getSTRUCTURE_ID() {
		return STRUCTURE_ID;
	}
	public void setSTRUCTURE_ID(long sTRUCTUREID) {
		STRUCTURE_ID = sTRUCTUREID;
	}
	public String getREPORT_HEADER_ITEM() {
		return REPORT_HEADER_ITEM;
	}
	public void setREPORT_HEADER_ITEM(String rEPORTHEADERITEM) {
		REPORT_HEADER_ITEM = rEPORTHEADERITEM;
	}
	public String getREPORT_HEADER_CODE() {
		return REPORT_HEADER_CODE;
	}
	public void setREPORT_HEADER_CODE(String rEPORT_HEADER_CODE) {
		REPORT_HEADER_CODE = rEPORT_HEADER_CODE;
	}
	public String getREPORT_HEADER_NAME() {
		return REPORT_HEADER_NAME;
	}
	public void setREPORT_HEADER_NAME(String rEPORT_HEADER_NAME) {
		REPORT_HEADER_NAME = rEPORT_HEADER_NAME;
	}
	
}
